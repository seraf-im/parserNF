namespace Pnhs\ParserXml;

class parserException extends \InvalidArgumentException
{
  public static $list = [
    0 => "O XML possui um modelo inválido ou não implementado, modelo xml {{msg}}"
  ];

  public static function wrong(int $code, ?string $msg = null)
  {
    $msg = self::replaceMsg(self::$list[$code], $msg);
    return new static($msg, $code);
  }

  private static function replaceMsg(string $input, string $msg): string
  {
    return str_replace('{{msg}}', $msg, $input);
  }
}
