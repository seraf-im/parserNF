namespace Pnhs\ParserNF\Validators;

use Pnhs\ParserNF\Utils\Decimal;
use Pnhs\ParserNF\Utils\CNPJ;

/**
 * Grupo YA. Informações de Pagamento
 */
class DetPag
{
    private int $indPag = 0;
    private string $tPag = '01';
    private string $vPag = '0';
    private int $tpIntegra = 2;
    private ?string $CNPJ = null;
    private ?string $tBand = null;
    private ?string $cAut = null;

    public function __construct()
    {
    }

    /**
     * __get
    *
    * @param string $name
    * @return mixed
    */
    public function __get(string $name): mixed
    {
        $method = 'get' . ucfirst($name);
        if (method_exists(__CLASS__, $method)) {
            return $this->$method();
        }
        return $this->$name;
    }

    /**
     * __set
    *
    * @param string $name
    * @param mixed $value
    * @return void
    */
    public function __set(string $name, mixed $value): void
    {
        $method = 'set' . ucfirst($name);
        if (method_exists(__CLASS__, $method)) {
            $this->$method($value);
        } else {
            $this->$name = $value;
        }
    }

    private function getTPag(): string
    {
        return str_pad($this->tPag, 2, "0", STR_PAD_LEFT);
    }

    private function getVPag(): string
    {
        return (new Decimal($this->vPag, 2, "."))->result();
    }

    private function getTBand(): ?string
    {
        if (is_null($this->tBand)) {
            return null;
        } else {
            return str_pad($this->tBand, 2, "0", STR_PAD_LEFT);
        }
    }

    private function setCnpj(string $value): void
    {

        if ((new CNPJ())->run($value)) {
            $this->CNPJ = $value;
        }
    }
}
