namespace Pnhs\ParserNF\Validators;

/**
 * Grupo GA. Autorização para obter XML
 */
class AutXML
{
    private array $autXML = [];

    public function __construct()
    {
    }

    /**
    * __get
    *
    * @param string $name
    * @return mixed
    */
    public function __get(string $name): mixed
    {
        $method = 'get' . ucfirst($name);
        if (method_exists(__CLASS__, $method)) {
            return $this->$method();
        }
        return $this->$name;
    }

    public function addDocument(string $document): void
    {
        if (strlen($document) == 11) {
            array_push($this->autXML, [
                'cpf' => $document
            ]);
        }

        if (strlen($document) == 14) {
            array_push($this->autXML, [
                'cnpj' => $document
            ]);
        }
    }
}
