namespace Pnhs\ParserNF\layout;

use stdClass;
use Pnhs\ParserNF\enums\{
  Uf,
};

class ga
{
  public static function run($data): array
  {
    $parser = $data->NFe->infNFe->autXML;
    $return = [];
    $i = 0;

    if (!$parser)
      return [];

    foreach ($parser as $item) {
      $std = new stdClass;
      if ($item->CNPJ)
        $std->cnpj                                = (string) $item->CNPJ;
      if ($item->CPF)
        $std->cpf                                 = (string) $item->CPF;

      $return[$i] = $std;
      $i++;
    }

    return $return;
  }
}
