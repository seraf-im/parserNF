namespace Pnhs\ParserNF\layout;

use Pnhs\ParserNF\enums\IpiCst;
use stdClass;

class p extends layout
{
  public static function run(object $data, int $numero_item, $std = new stdClass): stdClass
  {
    $parser = $data->NFe->infNFe->det[$numero_item - 1]->imposto->II;

    if (!$parser)
      return $std;

    $std->vBC             = self::tag((string) $parser->vBC, 'vBC não informado', 'P02', 1);
    $std->vDespAdu        = self::tag((string) $parser->vDespAdu, 'vDespAdu não informado', 'P03', 1);
    $std->vII             = self::tag((string) $parser->vII, 'vII não informado', 'P04', 1);
    $std->vIOF            = self::tag((string) $parser->vIOF, 'vIOF não informado', 'P05', 1);

    return $std;
  }
}
