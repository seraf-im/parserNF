namespace Pnhs\ParserNF\layout;

use stdClass;

class yb extends layout
{
  public static function run(object $data): stdClass
  {
    $std = new stdClass;

    $parser = $data->NFe->infNFe->infIntermed;

    if (!$parser)
      return $std;

    $std->CNPJ            = self::tag((string) $parser->CNPJ, 'CNPJ não informado', 'YB02', 1);
    $std->idCadIntTran    = self::tag((string) $parser->idCadIntTran, 'idCadIntTran não informado', 'YB03', 1);

    return $std;
  }
}
