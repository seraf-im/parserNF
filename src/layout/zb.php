namespace Pnhs\ParserNF\layout;

use stdClass;

class zb extends layout
{
  public static function run(object $data): stdClass
  {
    $std = new stdClass;

    $parser = $data->NFe->infNFe->compra;

    if (!$parser)
      return $std;

    $std->xNEmp           = self::tag((string) $parser->xNEmp, 'xNEmp não informado', 'ZB02', 0);
    $std->xPed            = self::tag((string) $parser->xPed, 'xPed não informado', 'ZB03', 0);
    $std->xCont           = self::tag((string) $parser->xCont, 'xCont não informado', 'ZB04', 0);

    return $std;
  }
}
