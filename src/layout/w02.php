namespace Pnhs\ParserNF\layout;

use stdClass;

class w02 extends layout
{
  public static function run(object $data): stdClass
  {
    $std = new stdClass;

    $parser = $data->NFe->infNFe->total->retTrib;

    if (!$parser)
      return $std;

    $std->vRetPIS         = self::tag((string) $parser->vRetPIS, 'vRetPIS não informado', 'W24', 0);
    $std->vRetCOFINS      = self::tag((string) $parser->vRetCOFINS, 'vRetCOFINS não informado', 'W25', 0);
    $std->vRetCSLL        = self::tag((string) $parser->vRetCSLL, 'vRetCSLL não informado', 'W26', 0);
    $std->vBCIRRF         = self::tag((string) $parser->vBCIRRF, 'vBCIRRF não informado', 'W27', 0);
    $std->vIRRF           = self::tag((string) $parser->vIRRF, 'vIRRF não informado', 'W28', 0);
    $std->vBCRetPrev      = self::tag((string) $parser->vBCRetPrev, 'vBCRetPrev não informado', 'W29', 0);
    $std->vRetPrev        = self::tag((string) $parser->vRetPrev, 'vRetPrev não informado', 'W30', 0);

    return $std;
  }
}
