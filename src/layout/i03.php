namespace Pnhs\ParserNF\layout;

use stdClass;

class i03 extends layout
{
  public static function run(object $data, int $numero_item, $std = new stdClass): stdClass
  {
    $parser = $data->NFe->infNFe->det->prod[$numero_item - 1]?->detExport;
    $return = [];

    if (!$parser)
      return $std;

    self::tag($parser, 'No máximo 500 declarações', 'I50', 0, 500);

    $i = 0;
    foreach ($parser as $item) {
      $r['nDraw']            = self::tag((string) $item->nDraw, 'nDraw não informado', 'I51', 0);

      if ($item->exportInd)
        $r                   = self::exportInd($item->exportInd, $r);

      $std->$i = $r;
      $i++;
    }

    return $std;
  }

  private static function exportInd(object $parser, array $r): array
  {
    $r['nRE']                = self::tag((string) $parser->nRE, 'nRE não informado', 'I53', 0);
    $r['chNFe']              = self::tag((string) $parser->chNFe, 'chNFe não informado', 'I54', 1);
    $r['qExport']            = self::tag((string) $parser->qExport, 'qExport não informado', 'I55', 1);

    return $r;
  }
}
