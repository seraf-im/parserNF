namespace Pnhs\ParserNF\layout;

use stdClass;

class protocolo extends layout
{
  public static function run(object|bool $data): null|stdClass
  {
    if (!$data) return null;
    $parser = $data->protNFe->infProt;
    $std = new stdClass;

    $std->tpAmb = self::tag((string)$parser->tpAmb, 'tpAmb', 'tpAmb', 1);
    $std->verAplic = self::tag((string)$parser->verAplic, 'verAplic', 'verAplic', 1);
    $std->chNFe = self::tag((string)$parser->chNFe, 'chNFe', 'chNFe', 1);
    $std->dhRecbto = self::tag((string)$parser->dhRecbto, 'dhRecbto', 'dhRecbto', 1);
    $std->nProt = self::tag((string)$parser->nProt, 'nProt', 'nProt', 1);
    $std->digVal = self::tag((string)$parser->digVal, 'digVal', 'digVal', 1);
    $std->cStat = self::tag((string)$parser->cStat, 'cStat', 'cStat', 1);
    $std->xMotivo = self::tag((string)$parser->xMotivo, 'xMotivo', 'xMotivo', 1);

    return $std;
  }
}
