namespace Pnhs\ParserNF\layout;

use Pnhs\ParserNF\enums\ArmaTpArma;
use stdClass;

class l extends layout
{
  public static function run(object $data, int $numero_item, $std = new stdClass): stdClass
  {
    $parser = $data->NFe->infNFe->det->prod[$numero_item - 1]?->arma;

    if (!$parser)
      return $std;

    $std->tpArma           = ArmaTpArma::from((int) self::tag((string) $parser->tpArma, 'tpArma não informado', 'L02', 1));
    $std->nSerie           = self::tag((string) $parser->nSerie, 'nSerie não informado', 'L03', 1);
    $std->nCano            = self::tag((string) $parser->nCano, 'nCano não informado', 'L04', 1);
    $std->descr            = self::tag((string) $parser->descr, 'descr não informado', 'L05', 1);

    return $std;
  }
}
